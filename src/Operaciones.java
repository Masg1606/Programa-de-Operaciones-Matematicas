public class Operaciones {
	
	public int suma (int a, int b) {
		
		int resultado = a + b;
		
		return resultado;
		
	}
	
	public int resta (int a, int b) {
		
		int resultado = a - b;
		
		return resultado;
		
	}
	
	public int multiplicacion (int a, int b) {
		
		int resultado = a * b;
		
		return resultado;
		
	}
	
	public float divicion (float a, float b) {
		
		float resultado = a / b;
		
		return resultado;
	
	
	}
	
}